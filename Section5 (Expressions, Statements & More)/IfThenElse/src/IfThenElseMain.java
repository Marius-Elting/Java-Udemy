public class IfThenElseMain {
    public static void main(String[] args) {
        int score = 5000;

        if (score < 5000) {
            System.out.println("Your Score was less than 5000");
        } else if (score == 5000) {
            System.out.println("Your Score was 5000");
        } else {
            System.out.println("Your Score was more than 5000");
        }

    }
}
